package DSA.Exercise_1;

public class infineLoop {
    
    public static void main(String[] args) {

        int i=1;

        do {
            System.out.print("I Love You");
        }while(i==1); //(infine loop since i is always 1)if condition false then it will be exit.
        // while(true);
    }
}
